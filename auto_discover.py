#!/usr/bin/env python3
"""
自動發現台灣法規查詢工具
讓Claude自動了解可用的MCP工具並主動提供服務
"""

def introduce_law_tools():
    """介紹可用的法律查詢工具"""
    introduction = """
🏛️ **台灣法規查詢系統已就緒**

我現在可以為您提供以下法律查詢服務：

📚 **搜尋法規** (`search_law`)
- 根據法規名稱搜尋，例如："民法"、"刑法"
- 取得法規基本資訊和官方網址
- 支援精確匹配和模糊搜尋

📖 **完整法規內容** (`get_full_law`) 
- 取得完整法規的所有條文
- 結構化JSON格式，包含章節架構
- 支援按名稱或代碼查詢

📄 **單條條文查詢** (`get_single_article`)
- 查詢特定條文詳細內容
- 支援各種條文號格式 (如：1、16-1)
- 提供條文逐行內容

🔍 **關鍵字搜尋** (`search_by_keyword`)
- 在所有法條中搜尋特定關鍵字
- 高亮顯示匹配內容
- 可設定結果數量上限

---

**使用範例：**
- "請幫我查詢民法第1條"
- "搜尋包含'契約'的法條"
- "取得財政收支劃分法的完整條文"
- "查詢刑法相關規定"

您現在可以直接詢問任何台灣法律相關問題，我會自動使用適當的工具為您查詢！
    """
    
    return introduction

if __name__ == "__main__":
    print(introduce_law_tools())